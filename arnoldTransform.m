% Реализация преобразования Арнольда для изображения.

M = size(inputImage, 1);  % Размеры изображения
N = size(inputImage, 2);  % Размеры изображения

outputImage = zeros(M, N);  % Инициализация выходного изображения

% Убедитесь, что размеры изображения равны (квадратное изображение)
if M ~= N
    error('Входное изображение должно быть квадратным.');
end

% Матрица преобразования Арнольда (для квадратного изображения)
A = [1 1; 1 2];

% Выполнение преобразования заданное количество раз
for i = 1:iterations
for y = 1:M
    for x = 1:N
        % Новые координаты пикселя
        newCoordinates = mod(A * [x; y] - 1, M) + 1; 
        newX = newCoordinates(1);
        newY = newCoordinates(2);
        % Перемещение пикселя
        outputImage(newY, newX) = inputImage(y, x);
    end
end

% Обновление входного изображения для следующей итерации
inputImage = outputImage;
end
